#Simple Math

n1 = int(input("What is the first number? "))
n2 = int(input("What is the second number? "))
print(n1, "+", n2, "=", n1+n2, sep = " ", end = "\n")
print(n1, "-", n2, "=", n1-n2, sep = " ", end = "\n")
print(n1, "*", n2, "=", n1*n2, sep = " ", end = "\n")
print(n1, "/", n2, "=", n1/n2, sep = " ", end = "\n")

